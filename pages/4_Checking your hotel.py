import streamlit as st

st.title("Making bee hotel observations")

st.subheader("How and when to check your hotel?")
st.write("""
To check your observation hotel, you will need to open the hotel by removing the curtain cord from the hotel and removing the lid panel.
""")
st.write("""
It would be great if the hotel/s could be checked once a day, in the evening (when it is dark and the bees are within the hotel). If you are super keen, you could check twice a day, preferably once in the middle of the day and once in the evening. 
""")
st.write("""
For each observation you need to take a clear photograph that is time stamped with the hotel code and the nest tunnel letter clearly visible. This will allow us to identify the number of tunnels, size of the nest hole used, brood production, false cells, sex ratios, activity window of the species and the unique identifier code of the hotel. 
""")
st.write("""
The photograph and data will then need to be uploaded to the data portal.
""")
st.write("""
We do not want the task of checking and uploading the data to be onerous, and we realise that there may be periods where you are away and can not check the hotel. This is absolutely fine, please just check it as regularly as you are able (keeping in mind this is a maximum of twice per day). 
""")

st.subheader("Importance of checking your data")
st.write("""
Data will be uploaded through the data portal [here](#).
""")
st.write("""
Please check that the image associated with the data that you are uploading is correct.
""")
st.write("""
This will ensure that the data we collect over time for each hotel is accurate and correct.
""")
st.write("""
When you open the data portal, you will see that there is a drop-down box with the hotel numbers that you have been assigned.
""")
st.write("""
Please select the hotel number that you want to enter data for.
""")
st.write("""
You will then see that there are a series of questions that you will be prompted to answer, some of which are mandatory. 
""")
st.write("""
To ease data collection, a number of the answers will prefill with your last data entry. 
""")
st.write("""
It is therefore very important to check the data you upload is correct. Please check this before submitting your data each time you upload an observation.
""")
st.write("""
Once uploaded the submission number will be the same for the individual hotel over time, the observation number will be unique for the individual nest within the hotel. 
""")
st.write("""
If you have made a mistake in the data you have uploaded, please get in contact with your contact person. 
""")

